// Typography sizes calculator

function setTypography() {
  let paragraphSize = 20;
  if (window.innerWidth > 1200) {
    paragraphSize = 20;
  } else {
    paragraphSize = Math.round((((window.innerWidth - 400) / 800) * 4.0) + 16);
  };
  if (window.innerWidth < 400) {
    paragraphSize = 16;
  };
  document.documentElement.style.setProperty('--paragraphSize', paragraphSize + 'px');
}

window.addEventListener('resize', function(event) {
  setTypography();
}, true);